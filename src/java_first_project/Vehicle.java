package java_first_project;

public class Vehicle {
	private String name;
	private int wheel;
	private String color;
	private int passengers;
	
	public Vehicle() {
		
	}
	public Vehicle(String name, int wheel, String color, int passengers) {
		this.name = name;
		this.wheel = wheel;
		this.color = color;
		this.passengers = passengers;
	}
}
